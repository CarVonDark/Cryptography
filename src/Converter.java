
public class Converter {
	String input;
	
	public Converter(String input) {
		this.input = input;
	}
	
	public String convert() {
		return this.input;
	}
	
	public String getInput() {
		return this.input;
	}

	public String decrypt() {
		return this.input;
	}

	public String encrypt() {
		return this.input;
	}
}
